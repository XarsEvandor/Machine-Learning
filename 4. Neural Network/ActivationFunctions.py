import numpy as np


# --------------------------------------------------------------------------------------
def Relu(x):
  return 0.0 if x < 0.0 else x # [PYTHON] a pythonic inline if else
# --------------------------------------------------------------------------------------
def ReluDerivative(x):
  return 0.0 if x < 0.0 else 1.0   # [PYTHON] a pythonic inline if else
# --------------------------------------------------------------------------------------


# --------------------------------------------------------------------------------------
def Sigmoid(x):
  y = 1/(1 + np.exp(-x)) 
  return y
# --------------------------------------------------------------------------------------
def SigmoidDerivative(x):
  y = Sigmoid(x)*(1 - Sigmoid(x))
  return y
